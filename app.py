class MainWindow(Window):

    def __init__(self, model, shapes, frame_rate, cell_size=10):
        self.window_width = shapes[0].shape[0] * cell_size
        self.window_height = shapes[0].shape[1] * cell_size
        super().__init__(self.window_width, self.window_height)

        self.shapes = shapes
        self.frame_rate = frame_rate
        self.index = 0
        self.increasing = True
        self.current = shapes[self.index]
        self.next = shapes[self.index + 1]
        self.model = model
        self.cell_size = cell_size
        self.parameters = (
            self.cell_size, self.window_width, self.window_height)
        self.ca = model(shapes[0], shapes[0], *self.parameters)
        self.key_map = {
            key.Q: lambda: self.close(),
            key._1: lambda: self._handle_1_key(),
            key._2: lambda: self._handle_2_key(),
            key._3: lambda: self._handle_3_key(),
            key._4: lambda: self._handle_4_key()
        }
        # print(self.key_map)
        # clock.schedule_interval(self.update, frame_rate)

    def _handle_1_key(self):
        # self.ca = self.model(
        #     self.shapes[self.current], self.shapes[0], *self.parameters)
        clock.schedule_interval(self.update, self.frame_rate)
            

    def _handle_2_key(self):
        self.ca = self.model(
            self.shapes[self.current], self.shapes[1], *self.parameters)
        self.current = 1

    def _handle_3_key(self):
        self.ca = self.model(
            self.shapes[self.current], self.shapes[2], *self.parameters)
        self.current = 2

    def _handle_4_key(self):
        self.ca = self.model(
            self.shapes[self.current], self.shapes[3], *self.parameters)
        self.current = 3

    def update(self, _):
        u = self.ca.update()
        if u:
            self.ca = self.model(self.current, self.next, *self.parameters)
            self.current = self.next
            if self.increasing:
                self.index += 1
            else:
                self.index -= 1
            if self.index == len(self.shapes):
                self.index -= 2
                self.increasing = False
            elif self.index == -1:
                self.index += 2
                self.increasing = True
            self.next = self.shapes[self.index]

    def on_draw(self):
        self.clear()
        self.ca.draw()

    def on_key_press(self, symbol, _):
        try:
            self.key_map[symbol]()
        except:
            if symbol == 50:
                self._handle_2_key()
            print(symbol)


